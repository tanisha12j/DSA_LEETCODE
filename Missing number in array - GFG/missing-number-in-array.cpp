class Solution{
  public:
    int missingNumber(vector<int>& array, int n) {
        // Your code goes here
        int i=1;
        int sum=0;
        while(i<=n)
        {
            sum+=i;
            i++;
        }
        int s=0;
        for(auto i : array)
        {
            s+=i;
        }
        return sum-s;
    }
};

